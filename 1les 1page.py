print('Вы празднуете свой день рождения -', b_day)
age: int = input('Введите свой возраст -')
age = int(age)
print('Здравствуйте, ', name, surname, '\n',
      'Вы празднуете День рождения -', b_day, 'числа', '\n', 'Вам сейчас -', age, 'года')













