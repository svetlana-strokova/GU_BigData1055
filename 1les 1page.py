# Запросить у пользователя несколько чисел и строк, сохранить в переменные и вывести на экран

name: str = input('Введите свое имя -')
print('Вас зовут -', name)
surname: str = input('Введите свою фамилию -')  # ввод текста
# а можно написать: surname str(input('Введите свою фамилию -'))
print('Здравствуйте,', name, surname)
